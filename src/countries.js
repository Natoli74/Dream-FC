// Function to validate country name
export const isValidCountry = (countryName) => {
  if (!countryName) return false;
  const normalized = countryName.toLowerCase().trim();
  return COUNTRIES.hasOwnProperty(normalized);
};

// Get list of all country names for autocomplete/suggestions
export const getAllCountryNames = () => {
  return Object.keys(COUNTRIES)
    .map((key) => {
      // Capitalize first letter of each word
      return key
        .split(" ")
        .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
        .join(" ");
    })
    .sort();
};
